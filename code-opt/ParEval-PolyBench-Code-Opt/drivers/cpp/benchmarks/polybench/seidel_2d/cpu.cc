bool validate(Context *ctx, std::mt19937& engine) {
    const size_t TEST_SIZE = DRIVER_PROBLEM_SIZE;
    
    int n = TEST_SIZE;
    int tsteps = 500;

    std::vector<std::vector<double>> A_correct;
    std::vector<std::vector<double>> A_test;

    A_correct.resize(n);
    A_test.resize(n);

    for (int i = 0; i < n; i++) {
	A_correct[i].resize(n);
	A_test[i].resize(n);
    }

    // set up input
    for (int i = 0; i < n; i++) {
	for (int j = 0; j < n; j++) {
	    A_correct[i][j] = (i*(j+2) + 2) * 1.0 / n;
	    A_test[i][j] = (i*(j+2) + 2) * 1.0 / n;
	}
    }
    
    // compute correct result
    correct_kernel_seidel_2d(tsteps, n, A_correct);

    // compute test result
    submission::kernel_seidel_2d(tsteps, n, A_test);
    
    bool isCorrect = true;

    for (int i = 0; i < n; i++) {
	if (!fequal(A_correct[i], A_test[i], 1e-4)) {
	    isCorrect = false;
	    break;
	}
    }

    if (!isCorrect) {
	return false;
    }

    return true;
}

