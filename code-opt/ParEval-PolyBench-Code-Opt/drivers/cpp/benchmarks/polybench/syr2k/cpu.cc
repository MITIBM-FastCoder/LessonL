void reset(Context *ctx, std::mt19937& engine) {
    ctx->alpha = 1.5;
    ctx->beta = 1.2;

    for (int i = 0; i < ctx->n; i++) {
	for (int j = 0; j < ctx->m; j++) {
	    ctx->A[i][j] = ((i*j+1)%ctx->n) * 1.0 / ctx->n;
	    ctx->B[i][j] = ((i*j+2)%ctx->m) * 1.0 / ctx->m;
	}
    }

    for (int i = 0; i < ctx->n; i++) {
	for (int j = 0; j < ctx->n; j++) {
            ctx->C[i][j] = ((i*j+3)%ctx->n) * 1.0 / ctx->m;
	}
    }
}

