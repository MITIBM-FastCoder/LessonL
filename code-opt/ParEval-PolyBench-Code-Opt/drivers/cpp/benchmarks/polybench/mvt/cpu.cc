void reset(Context *ctx, std::mt19937& engine) {
    for (int i = 0; i < ctx->n; i++) {
	ctx->x1[i] = (i % ctx->n) * 1.0 / ctx->n;
	ctx->x2[i] = ((i + 1) % ctx->n) * 1.0 / ctx->n;
	ctx->y_1[i] = ((i + 3) % ctx->n) * 1.0 / ctx->n;
	ctx->y_2[i] = ((i + 4) % ctx->n) * 1.0 / ctx->n;
	for (int j = 0; j < ctx->n; j++) {
	    ctx->A[i][j] = (i * j % ctx->n) * 1.0 / ctx->n;
	}
    }
}

Context *init() {
    Context *ctx = new Context();

    ctx->n = DRIVER_PROBLEM_SIZE;

    ctx->x1.resize(ctx->n);
    ctx->x2.resize(ctx->n);
    ctx->y_1.resize(ctx->n);
    ctx->y_2.resize(ctx->n);

    ctx->A.resize(ctx->n);

    for (int i = 0; i < ctx->n; i++) {
	ctx->A[i].resize(ctx->n);
    }

    // reset(ctx);
    return ctx;
}

