bool validate(Context *ctx, std::mt19937& engine) {
    const size_t TEST_SIZE = DRIVER_PROBLEM_SIZE;

    int n = TEST_SIZE;

    std::vector<std::vector<double>> A;
    std::vector<std::vector<double>> A_test;

    A.resize(n);
    A_test.resize(n);

    for (int i = 0; i < n; i++) {
	A[i].resize(n);
	A_test[i].resize(n);
    }

    /* Make the matrix positive semi-definite. */
    /* not necessary for LU, but using same code as cholesky */
    std::vector<std::vector<double>> B;
    B.resize(ctx->n);
    for (int i = 0; i < ctx->n; i++) {
	B[i].resize(ctx->n);
    }

    // set up input
    for (int i = 0; i < n; i++) {
	for (int j = 0; j <= i; j++) {
	    A[i][j] = (-j % n) * 1.0 / n + 1;
	}
	for (int j = i + 1; j < n; j++) {
	    A[i][j] = 0.0;
	}
	A[i][i] = 1.0;
    }

    for (int r = 0; r < n; r++) {
	for (int s = 0; s < n; s++) {
	    B[r][s] = 0.0;
	}
    }

    for (int t = 0; t < n; t++) {
	for (int r = 0; r < n; r++) {
	    for (int s = 0; s < n; s++) {
		B[r][s] += A[r][t] * A[s][t];
	    }
	}
    }

    for (int r = 0; r < n; r++) {
	for (int s = 0; s < n; s++) {
	    A[r][s] = B[r][s];
	    A_test[r][s] = A[r][s];
	}
    }
	
    // compute correct result
    correct_kernel_lu(n, A);

    // compute test result
    submission::kernel_lu(n, A_test);
    
    bool isCorrect = true;

    for (int i = 0; i < n; i++) {
	if (!fequal(A[i], A_test[i], 1e-4)) {
	    isCorrect = false;
	    break;
	}
    }

    if (!isCorrect) {
	return false;
    }

    return true;
}

